            <h1>Sữa có tốt cho sức khỏe như chúng ta nghĩ không?</h1>
            <div class="meta-info">
                <span>📅 05-12-2024 11:04</span>
                <span> | </span>
                <span class="author">Thực phẩm</span>
            </div>
            <p class="text">Nhiều người bao gồm sữa trong chế độ ăn uống của họ, nhưng chỉ một số ít người đáp ứng số lượng được đề nghị hàng ngày. Các chuyên gia hiện đang kêu gọi chúng ta suy nghĩ lại về các khuyến nghị này và giải thích tại sao sữa có thể không lành mạnh như chúng ta nghĩ.</p>
            <p class="text">Hình ảnh sữa bò sữa đã có một chút thay đổi, với các loại yến mạch, hạnh nhân và sữa đậu nành được ca ngợi là lựa chọn thay thế thân thiện với môi trường.</p>
            <p class="text">Nhưng đối với nhiều người ở mọi lứa tuổi, sữa bò vẫn là một loại thức uống được yêu thích – vượt qua cả ngũ cốc, như một người bạn đồng hành với cà phê, hoặc thưởng thức như một thức uống yêu thích trước khi đi ngủ.</p>
            <p class="text">Hướng dẫn chế độ ăn uống Hoa Kỳ 2015 – 2020 khuyến nghị rằng các cá nhân từ 9 tuổi trở lên nên tiêu thụ tương đương 3 cốc với các sản phẩm sữa không béo và ít béo (1%). Theo hướng dẫn, được kết hợp bởi Bộ Y tế và Dịch vụ Nhân sinh Hoa Kỳ và Bộ Nông nghiệp Hoa Kỳ, bao gồm sữa, sữa chua, phô mai và sữa đậu nành tăng cường.</p>
            <p class="text">Tuy nhiên, lượng sữa trung bình mà người lớn ở Hoa Kỳ tiêu thụ là khoảng 1,6 cốc mỗi ngày, thiếu so với mức khuyến nghị.</p>
            <p class="text">Điều đó có nghĩa là tất cả chúng ta nên tìm cách tăng tiêu thụ sữa?</p>
            <p class="text">Các chuyên gia viết trên Tạp chí Y học New England không nghĩ vậy. Thay vào đó, họ đặt câu hỏi về chất lượng của bằng chứng củng cố các khuyến nghị này và đề xuất các nguồn thay thế để cung cấp cho chúng ta các chất dinh dưỡng cần thiết cho sức khỏe của chúng ta.</p>
            <h1>Sức mạnh của các bằng chứng là “giới hạn”</h1>
            <img src="assets/images/post/sua-co-tot-cho-suc-khoe-nhu-chung-ta-nghi-khong-01.jpg" alt="Hospital News Image">
            <p class="caption">Liệu chúng ta có cần uống đủ sữa như khuyến nghị?</p>
            <p class="text">Cuộc tranh luận về sữa, trên thực tế, không phải là một vấn đề mới.</p>
            <p class="text">Trở lại năm 2014, Connie M Weaver, giáo sư danh dự và trước đây là trưởng khoa Khoa học Dinh dưỡng tại Đại học Purdue ở West Lafayette, Indiana (Mỹ), đã viết một bài báo trên Tạp chí Dinh dưỡng Lâm sàng Hoa Kỳ nhấn mạnh việc thiếu bằng chứng chất lượng tốt để hỗ trợ việc sử dụng sữa.</p>
            <p class="text">Trong bài viết của mình, một phần, được tài trợ bởi Danone Institute International, Weaver đã đề cập đến những lý do lịch sử đằng sau tầm quan trọng của sữa đối với chế độ ăn uống của chúng ta.</p>
            <p class="text">“Thực phẩm bơ sữa đóng vai trò trung tâm trong hầu hết các khuyến nghị hướng dẫn chế độ ăn uống. Chúng cung cấp một lượng các chất dinh dưỡng thiết yếu và các thành phần có hoạt tính sinh học cho sức khỏe, khó có được trong chế độ ăn mà không có hoặc hạn chế sử dụng các sản phẩm từ sữa”, Weaver viết.</p>
            <p class="text">“Từ cuộc cách mạng nông nghiệp, khi các nguồn năng lượng chuyển từ thực phẩm thực vật có hàm lượng canxi tương đối cao trong chế độ ăn uống của người săn bắn sang trồng ngũ cốc với hàm lượng canxi thấp, nguồn canxi chủ yếu là sữa”, cô tiếp tục.</p>
            <p class="text">Sữa đã xuất hiện trong mỗi lần lặp lại Hướng dẫn chế độ ăn của Hoa Kỳ kể từ lần xuất bản đầu tiên vào năm 1917. Cứ sau 5 năm, Hướng dẫn chế độ ăn uống cho Ủy ban tư vấn người Mỹ cập nhật hướng dẫn, xem xét các bằng chứng có sẵn.</p>
            <p class="text">Weaver tham khảo nghiên cứu nêu bật cách tuân theo chế độ ăn không có sữa trong bối cảnh chế độ ăn phương Tây theo phong cách Hoa Kỳ khiến thanh thiếu niên ở độ tuổi 9 – 18 tuổi phải vật lộn để đạt được lượng canxi khuyến nghị.</p>
            <p class="text">Với mục đích đáp ứng lượng dinh dưỡng hàng ngày, sữa và phô mai đóng góp vào “46,3% canxi, 11,6% kali và 7,9% magiê trong chế độ ăn uống của người Mỹ”.</p>
            <h1>Các nghiên cứu về sữa có “hạn chế nghiêm trọng”</h1>
            <p class="text">Trong bài viết của họ, các giáo sư nhấn mạnh những đóng góp mà sữa có thể làm cho vô số khía cạnh sức khỏe của chúng ta.</p>
            <p class="text">Sức khỏe xương có lẽ là quen thuộc nhất với nhiều người.</p>
            <p class="text">Sữa là nguồn canxi dồi dào, một khoáng chất quan trọng để phát triển và duy trì chức năng xương tốt. Tuy nhiên, các nghiên cứu đặt ra các khuyến nghị hàng ngày về lượng sữa và lượng canxi mở rộng, chúng ta nên tiêu thụ là rất nhỏ.</p>
            <p class="text">“Các cơ sở cho các khuyến nghị của Hoa Kỳ về tiêu thụ sữa xuất phát từ các nghiên cứu đánh giá sự cân bằng của lượng canxi và bài tiết ở chỉ 155 người trưởng thành trong đó lượng canxi cần thiết để duy trì cân bằng là 741 mg mỗi ngày”, các giáo sư viết trong bài báo của họ.</p>
            <p class="text">“Ngoài quy mô nhỏ, những nghiên cứu về sự cân bằng này còn có những hạn chế nghiêm trọng khác, bao gồm thời gian ngắn (2 đến 3 tuần) và việc sử dụng canxi theo thói quen cao”, họ tiếp tục.</p>
            <p class="text">Bằng chứng không hỗ trợ tiêu thụ sữa để giảm nguy cơ gãy xương hông, họ giải thích thêm.</p>
            <p class="text">Ngược lại, họ chỉ ra rằng các quốc gia có lượng sữa và canxi cao cũng có tỷ lệ gãy xương hông cao nhất.</p>
            <p class="text">Họ tham khảo một nghiên cứu năm 2014 trên JAMA Pediatrics của Tiến sĩ Willett đã kiểm tra nguy cơ gãy xương hông ở nam giới liên quan đến lượng sữa họ uống trong những năm tuổi thiếu niên.</p>
            <p class="text">Kết quả cho thấy tiêu thụ sữa cao hơn dẫn đến tăng nguy cơ gãy xương hông sau này trong cuộc sống.</p>
            <p class="text">Làm thế nào chúng ta phát triển nhanh và cao là hai ví dụ khác. Nghiên cứu đã thiết lập một liên kết giữa những điều này và tiêu thụ sữa. Tuy nhiên, các giáo sư kêu gọi thận trọng khi đưa ra kết luận vào thời điểm này.</p>
            <p class="text">“Những hậu quả về sức khỏe của sự tăng trưởng nhanh và chiều cao trưởng thành lớn hơn rất phức tạp”, họ viết. “Tầm vóc cao có liên quan đến nguy cơ mắc bệnh tim mạch thấp hơn nhưng nguy cơ mắc nhiều bệnh ung thư, gãy xương hông và thuyên tắc phổi cao hơn”.</p>
